void main(List<String> args) {
  var i = 4;

  switch (i) {
    case 1:
      print("one");
      break;
    case 4:
      print("four");
      break;
  }

  var j = 'a';
  switch (j) {
    case 'a':
      print("Apple");
      break;
    case 'o':
      print("Orange");
      break;
    case 'c':
      print("Chery");
  }
}
